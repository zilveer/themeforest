				$_typography_val = '"'.$_typography_val.'"';
			}
			$_variables[$_default_variable_name[$i].'-'.$_typography_option[$val_i]] = $_typography_val;
		}
	}
}

if (count($_variables)>0) {
	foreach ( $_variables as $k=>$v ) {
		if(!empty($v)) {
			echo "{$k}: {$v};" . PHP_EOL;
		}
	}
