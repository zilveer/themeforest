function set_ocmx_fonts(){
	jQuery(".color-picker-box").each(function(){
						
		var id = jQuery(this).attr("id").replace("_color_picker", "");
		var overview = "#"+id+"_overview";	
		var color = "#"+id+"_color";
		var size = "#"+id+"_size";		
		var style_div = "div[rel="+id+"_style]";
		var style = "input[name="+id+"_style]:checked";
		var finalStyle = "";
		
		if(jQuery(color).attr("value") !== "")
			{finalStyle += "color: "+jQuery(color).attr("value")+";"};
	
		if(jQuery(size).attr("value") !== "")
			{finalStyle += "font-size: "+jQuery(size).attr("value")+"px;"};
			
		jQuery(style_div).children("div").each(function(){
			div = jQuery(this);			
			divstyle = "font-family: "+div.css("fontFamily")+"; "+finalStyle;
			div.attr("style", divstyle);
		});
	
		if(jQuery(style).attr("value") !== "")
			{finalStyle += "font-family: "+jQuery(style).attr("value")+";"};
		
		jQuery(overview).attr("style", finalStyle);
	});

};